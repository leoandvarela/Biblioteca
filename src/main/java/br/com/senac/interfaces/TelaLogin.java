    private void BotaoEntrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotaoEntrarActionPerformed

        String login = InserirLogin.getText(); 
        String senha = InserirSenha.getText(); 

        UsuarioDAO usuarioDAO = new UsuarioDAO(); 
        Usuario usuario = usuarioDAO.login(login, senha); 

        List<String> listaErros = new ArrayList<>(); 

        if (login == null || login.trim().isEmpty()) {
            listaErros.add("Informe o login!"); 
        }
        if (senha == null || senha.trim().isEmpty()) {
            listaErros.add("Informe a senha!"); 
        }

        if (!listaErros.isEmpty()) {
            String mensagemErro = "Para entrar, corrija o(s) erro(s) abaixo:\n\n";
            for (String erro : listaErros) {
                mensagemErro += erro + "\n"; 
            }
            JOptionPane.showMessageDialog(this, mensagemErro); 
        } else {
            if (usuario != null) {
                TelaCadastrar tela = new TelaCadastrar(usuario, true);
                tela.setVisible(true);
            } else {
                JOptionPane.showMessageDialog(null, "Login e/ou Senha inv√°lidos!!!"); 
        }
      }
    }//GEN-LAST:event_BotaoEntrarActionPerformed

