    private void BotaoExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotaoExcluirActionPerformed
        try {
            if (TabelaLivros.getSelectedRow() >= 0) { 
                String id = (String) TabelaLivros.getValueAt(TabelaLivros.getSelectedRow(), 0);

                int resposta = JOptionPane.showConfirmDialog(this, "Deseja mesmo excluir o registro " + id + "?");
                if (resposta == 0) { 
                    BibliotecaDAO bibliotecaDAO = new BibliotecaDAO();            
                    bibliotecaDAO.excluir(Integer.parseInt(id));
                    JOptionPane.showMessageDialog(this, "Registro excluído com sucesso");
                   
                    BotaoPesquisarActionPerformed(evt);
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Ocorreu uma falha:\n" + e.getMessage());
        }       
    }//GEN-LAST:event_BotaoExcluirActionPerformed

