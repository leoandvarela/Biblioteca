     public void preencheTabela(List<Biblioteca> biblioteca){
        String colunas[] = {"Id", "Autor", "Nome do livro", "Data de publicação", "Gênero"};
        String dados[][] = new String[biblioteca.size()][colunas.length];

        int i = 0;
        for (Biblioteca b : biblioteca) {
            dados[i] = new String[]{ 
                String.valueOf(b.getId()), 
                b.getAutor(),
                b.getGenero(),
                b.getdataPublicacao(),
                b.getnomeLivro(),            
            };
            i++;
        }

        DefaultTableModel model = new DefaultTableModel(dados, colunas);
        TabelaLivros.setModel(model);
    }  

